    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //se crea una variable dato y se usa joption pane para abrir una ventana emergente
        int retiro = Integer.parseInt(JOptionPane.showInputDialog("Digite la cantidad: "));
        
        int dinero = Integer.parseInt(saldon.getText());
        
        if(retiro < dinero){
            int retirar = dinero - retiro;
            saldon.setText(String.valueOf(retirar));
        }
        else{
            JOptionPane.showMessageDialog(null, "La cantidad es mayor al dinero en la cuenta!!!!");
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

